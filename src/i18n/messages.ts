export type AppLanguage = keyof typeof messages;
export type MessageCatalog = (typeof messages)[AppLanguage];

export function resolveAppLanguage(language?: string): AppLanguage {
  return language === "es" ? "es" : "en";
}

export function getMessages(language?: string): MessageCatalog {
  return messages[resolveAppLanguage(language)];
}
